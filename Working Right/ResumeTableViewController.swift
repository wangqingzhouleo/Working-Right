    func loadData()
    {
        if let path = NSBundle.mainBundle().pathForResource("Resume", ofType: "json")
        {
            do
            {
                let jsonData = try NSData(contentsOfFile: path, options: .DataReadingMappedIfSafe)
                let result = try NSJSONSerialization.JSONObjectWithData(jsonData, options: .MutableContainers) as! [NSDictionary]
                
                for item in result
                {
                    let feature = item["feature"] as! String
                    resumeList.append(feature)
                }
            }
            catch let error
            {
                print(error)
            }
        }
    }

